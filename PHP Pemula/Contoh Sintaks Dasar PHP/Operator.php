<?php

// Operator pada PHP
// 1. Aritmatika ( +, -, /, *, % )
// 2. Penggabungan String (Concat/Concatenation)
$nama_depan = "Abdu";
$nama_akhir = "rahman";
echo $nama_depan . $nama_akhir;

// 3. Assignment (==, +=, -=, *=, /=, %=, .=)
$coba = "halo";
echo $coba .= "hai";

// 4. Perbandingan (<, >, <=, >=, ==, !=), menghasilkan nilai BOOLEAN
var_dump(1 < 5);


// 5. Identitas (===, !==), untuk mengecek masing" tipe data 
var_dump(1 === "1");


